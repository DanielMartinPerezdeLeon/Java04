    public void cambiarDatos(Cuenta c){
        areanumero.setText(Integer.toString(c.getNumero()));
        if(c.getPropietario() == null){
            areapropietario.setText("********");
        }else{
            areapropietario.setText(c.getPropietario());
        }
        areasaldo.setText(Float.toString(c.getSaldo()));
        areasaldominimo.setText(Float.toString(c.getSaldominimo()));
        areafecha.setText("Arreglar");//-----------------------------------------------------------
        
        if(c.getClass() == CuentaAhorro.class){
            CuentaAhorro cAux = (CuentaAhorro) c;
            ModificableTexto.setText("Interes Mensual:");
            ModificableBooleano.setText("Es Estudiante:");
            
            areaTexto.setVisible(true);
            areaBooleano.setVisible(true);
            
            areaTexto.setText(Float.toString(cAux.getInteresmensual()));
            if(cAux.isEstudiante())
                areaBooleano.setText("Si es.");
            else
                areaBooleano.setText("No es.");
            
        }else if(c.getClass() == CuentaCorriente.class){
            CuentaCorriente cAux = (CuentaCorriente) c;
            
            ModificableTexto.setText("Comision:");
            ModificableBooleano.setText("Tipo de Comision:");
            
            areaTexto.setVisible(true);
            areaBooleano.setVisible(true);
            
            areaTexto.setText(Float.toString(cAux.getComision()));
            if(cAux.isTipoComision())
                areaBooleano.setText("Semestral.");
            else
                areaBooleano.setText("Anual.");
            
            
        }else if(c.getClass() == CuentaInversion.class){
            CuentaInversion cAux = (CuentaInversion) c;
            ModificableTexto.setText("Beneficio:");
            ModificableBooleano.setText("Es Estudiante:");
            
            areaTexto.setVisible(true);
            areaBooleano.setVisible(true);
            
            areaTexto.setText(Float.toString(cAux.getBeneficio()));
            if(cAux.isEstudiante())
                areaBooleano.setText("Si es.");
            else
                areaBooleano.setText("No es.");
            
        }else{
            ModificableTexto.setText("******");
            ModificableBooleano.setText("******");
            areaTexto.setVisible(false);
            areaBooleano.setVisible(false);
        }
    }

    public void siguiente(Lista lista) {

        lista.setIndice(lista.getIndice() + 1);
        Cuenta actual = (Cuenta) lista.getVector()[lista.getIndice()].getDatos();

        cambiarDatos(actual);
        if (lista.getVector()[lista.getIndice() + 1] == null) {
            botonsiguiente.setVisible(false);
        }
        if (lista.getIndice() - 1 >= 0) {
            botonanterior.setVisible(true);
        }
    }
   
    public void anterior(Lista lista) {

        lista.setIndice(lista.getIndice() - 1);
        Cuenta actual = (Cuenta) lista.getVector()[lista.getIndice()].getDatos();

        cambiarDatos(actual);
        if (lista.getIndice() - 1 < 0) {
            botonanterior.setVisible(false);
        }
        if (lista.getVector()[lista.getIndice() + 1] != null) {
            botonsiguiente.setVisible(true);
        }
    }

